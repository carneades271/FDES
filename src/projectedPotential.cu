__global__ void projectedPotential_d ( cufftComplex* V, int Z,  params_t* params)
{
	const int i = blockIdx.x * blockDim.x + threadIdx.x;
	const int m1 = params->IM.m1;
	const int m2 = params->IM.m2;

	__shared__ float a[3];
	__shared__ float b[3];
	__shared__ float c[3];
	__shared__ float d[3];

	if ( threadIdx.x < 12 )
	{    parametersKirkland_d ( a, b, c, d, Z, threadIdx.x ); }
	__syncthreads ();

	if ( i < m1 * m2 )
	{
		int i1, i2;
        dbCoord ( i1, i2, i, m1 );
        iwCoordIp ( i1, m1 );
        iwCoordIp ( i2, m2 );

		const float d1 = 1e10f * params->IM.d1;
		const float d2 = 1e10f * params->IM.d2;

		float qsq = ( (float) i1 ) / ( d1 * ( (float) m1 ) );
		float Vz  = ( (float) i2 ) / ( d2 * ( (float) m2 ) );
		qsq = qsq * qsq + Vz * Vz;

		Vz  = a[0] / ( qsq + b[0] )  +  c[0] * expf( -d[0] * qsq ); // Unpacked for-loop, is a bit faster
		Vz += a[1] / ( qsq + b[1] )  +  c[1] * expf( -d[1] * qsq );
		Vz += a[2] / ( qsq + b[2] )  +  c[2] * expf( -d[2] * qsq );

		V[i].x = Vz * ( 4.78776452e-9f * params->EM.sigma ) / ( d1 * d2 * ( (float) ( m1*m2 ) ) ); 
		// 4.78776452e-9f = 2*pi*a0*e and has dimensions V*m*m, and sigma has dimensions 1/(Vm), 
		// divided by d1*d2 to account for the analytical integral on p. 252 of Kirkland's book, 
		// divided by m1*m2 to account for the renormalization of cufft
		// This expression is tested against the correct potential (in real space) in the MATLAB code used in 
		// Phys. Rev. Lett. 109 (2012) 245502 and Phys. Rev. B 87 (2013) 184108.

		V[i].y = 0.f;
	}
}


__global__ void normalizeProjectedPotential_d ( cufftComplex* V, float V0x, int size )
{
	const int i = blockIdx.x * blockDim.x + threadIdx.x;

	if ( ( i < size ) )
	{    V[i].x /= V0x * ( (float) size ); } // normalize to a total intensity of 1, devide by size to account for the renormalization of cufft.

}


